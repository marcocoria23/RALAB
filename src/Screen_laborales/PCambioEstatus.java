    public void cambioEstatus()
    {
         if (PEstatus.version.equals("1.0") && (PEstatus.BD.equals("Estatal")))
        {
            System.out.println("cambiando v1+estatal");
            est.V1CambioEstatus();
        
        }
        if (PEstatus.version.equals("2.0") && (PEstatus.BD.equals("Estatal")))
        {
            System.out.println("cambiando v2+estatal");
             est.V2CambioEstatus();
       
        }
        if (PEstatus.version.equals("3.0") && (PEstatus.BD.equals("Estatal")))
        {
            System.out.println("cambiando v3+estatal");
             est.V3CambioEstatus();
       
        }
        if (PEstatus.version.equals("Estructura_Definita") && (PEstatus.BD.equals("Estatal")))
        {
            System.out.println("cambiando estructura+definitiva+estatal");
             est.EDCambioEstatus();
       
        }
         if (PEstatus.version.equals("1.0") && (PEstatus.BD.equals("Federal")))
        {
        
             System.out.println("cambiando v1+federal");
              est.V1FedCambioEstatus();
        }
    }
    


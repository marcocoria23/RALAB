    //proceso que llena combo CorganoJur para asignar todas las claves de organo existentes en la bd
    public void llenaCombo(){
         
        if (version.equals("1.0") && (BD.equals("Estatal")) )
        {
        ArrayClave_organo=query.Clave_organo();    
        }
        if (version.equals("2.0") && (BD.equals("Estatal")))
        {
        ArrayClave_organo=queryNE.Clave_organoNE();    
        }
        if (version.equals("3.0") && (BD.equals("Estatal")))
        {
        ArrayClave_organo=V3queryNE.Clave_organoNE();    
        }
        if (version.equals("Estructura_Definitiva") && (BD.equals("Estatal")))
        {
        ArrayClave_organo=EDQuerys.Clave_organoNE();    
        }
        if (version.equals("1.0") && (BD.equals("Federal")))
        {
        ArrayClave_organo=V1FedQuerys.Clave_organoNE();    
        } 
         CorganoJur.removeAllItems();
         for (int i=0;i<ArrayClave_organo.size();i++){
          String Organo=(Arrays.toString(ArrayClave_organo.get(i)));
          CorganoJur.addItem(Organo.replace("[","").replace("]",""));
        }
    }
    
